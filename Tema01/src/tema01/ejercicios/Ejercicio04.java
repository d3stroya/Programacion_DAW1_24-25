public class Ejercicio04 {
    public static void main(String[] args) {            
        // Declaro las variables
        int dato1, dato2, dato3;
        int resultado;
        
        // Inicializo las variables
        dato1 = 20;
        dato2 = 10;
        dato3 = 30;
        
        // Suma
        resultado = dato1 + dato2 + dato3;
        System.out.println(dato1 + " + " + dato2 + " + " + dato3 +  " = " + resultado);
        
        // Resta
        resultado = dato1 - dato2 - dato3;
        System.out.println(dato1 + " - " + dato2 + " - " + dato3 +  " = " + resultado);
        
        // Producto        
        resultado = dato1 * dato2 * dato3;
        System.out.println(dato1 + " * " + dato2 + " * " + dato3 +  " = " + resultado);
        
        // Cociente
        resultado = dato1 / dato2 / dato3;
        System.out.println(dato1 + " / " + dato2 + " / " + dato3 +  " = " + resultado);
    }
}
