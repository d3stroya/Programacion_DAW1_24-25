public class Ejercicio03 {
    public static void main(String[] args) {
        // Declaro las variables
        int dato1, dato2;
        int resultado;
        
        // Inicializo las variables
        dato1 = 20;
        dato2 = 10;
        
        // Suma
        resultado = dato1 + dato2;
        System.out.println(dato1 + " + " + dato2 + " = " + resultado);
        
        // Resta
        resultado = dato1 - dato2;
        System.out.println(dato1 + " - " + dato2 + " = " + resultado);
        
        // Producto        
        resultado = dato1 * dato2;
        System.out.println(dato1 + " * " + dato2 + " = " + resultado);
        
        // Cociente
        resultado = dato1 / dato2;
        System.out.println(dato1 + " / " + dato2 + " = " + resultado);
    }
}
